var text = document.getElementById("text");

//Pour afficher le texte en bleu:
function blueText(){
  text.style.color = "#3C73E7";
}

//Pour afficher le texte en vert:
function greentext(){
  text.style.color = "#18B55F";
}

//Pour afficher le texte en rouge:
function redText(){
  text.style.color = "#E73C4D";
}

//Pour afficher le texte en bleu:
function displayBlock(){
  text.style.display = "#3C73E7";
}
